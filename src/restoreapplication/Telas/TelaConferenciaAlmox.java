public void carregaProdutos() {
        Thread thread = new Thread() {
            @Override
            public void run() {
                jTextArea1.append("PRODUTOS COM ERRO NO ALMOXARIFADO EMP: " + empresa + " ALMOX: " + almox + "\n");
                ProdutoDAO produtoDAO = new ProdutoDAO();
                ArrayList<Produto> listaProdutos = new ArrayList();
                try {
                    listaProdutos = produtoDAO.buscaCodigos(empresa);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Verifica.class.getName()).log(Level.SEVERE, null, ex);
                }
                int tamanho;
                tamanho = listaProdutos.size();
                jProgressBar1.setMaximum(tamanho);
                jProgressBar1.setMinimum(0);
                int indice = 0;
                int erros = 0;
                for (Produto prod : listaProdutos) {
                    if (interromper) {
                        break;
                    }
                    String codString = prod.getCODIGO();

                    indice++;
                    jTextField2.setText(codString);
                    jProgressBar1.setValue(indice);
                    boolean existe = false;
                    try {
                        existe = produtoDAO.consultaTestEstoque(empresa, almox, codString);
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(TelaConferenciaAlmox.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    if(!existe){
                        erros++;
                    jTextArea1.append("-----------------------------------------------------------" + "\n");
                    jTextArea1.append("Produto        : " + codString + "\n");
                    jTextArea1.append("NÃ£o possui registro na testestoque para o almox selecionado \n");
                    }
                }
                jTextArea1.append("-----------------------------------------------------------" + "\n");
                jTextArea1.append("PROCESSO CONCLUIDO.\n");
                jTextArea1.append("-----------------------------------------------------------" + "\n");
                jTextArea1.append(erros + " - PRODUTOS COM ERRO.\n");
                jTextArea1.append("-----------------------------------------------------------" + "\n");
                jTextArea1.setCaretPosition(jTextArea1.getText().length());
            }
        };

        thread.start();

    }
