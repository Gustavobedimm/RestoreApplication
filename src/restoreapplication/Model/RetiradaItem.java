package restoreapplication.Model;
public class RetiradaItem {
    private String empresa;
    private String idretirada;
    private String identificador;
    private String idproduto;
    private String idpedido;
    private String qtde;
    private String almox;
    private String status;

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getIdretirada() {
        return idretirada;
    }

    public void setIdretirada(String idretirada) {
        this.idretirada = idretirada;
    }

    public String getIdentificador() {
        return identificador;
    }

    public void setIdentificador(String identificador) {
        this.identificador = identificador;
    }

    public String getIdproduto() {
        return idproduto;
    }

    public void setIdproduto(String idproduto) {
        this.idproduto = idproduto;
    }

    public String getIdpedido() {
        return idpedido;
    }

    public void setIdpedido(String idpedido) {
        this.idpedido = idpedido;
    }

    public String getQtde() {
        return qtde;
    }

    public void setQtde(String qtde) {
        this.qtde = qtde;
    }

    public String getAlmox() {
        return almox;
    }

    public void setAlmox(String almox) {
        this.almox = almox;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
