package restoreapplication.Model;
public class TransfEmpresas {
    private String id;
    private String data;
    private String hora;
    private String usuario;
    private String status;
    private String obs;
    private String empresaOrigem;
    private String almoxOrigem;
    private String empresaDestino;
    private String almoxDestino;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getObs() {
        return obs;
    }

    public void setObs(String obs) {
        this.obs = obs;
    }

    public String getEmpresaOrigem() {
        return empresaOrigem;
    }

    public void setEmpresaOrigem(String empresaOrigem) {
        this.empresaOrigem = empresaOrigem;
    }

    public String getAlmoxOrigem() {
        return almoxOrigem;
    }

    public void setAlmoxOrigem(String almoxOrigem) {
        this.almoxOrigem = almoxOrigem;
    }

    public String getEmpresaDestino() {
        return empresaDestino;
    }

    public void setEmpresaDestino(String empresaDestino) {
        this.empresaDestino = empresaDestino;
    }

    public String getAlmoxDestino() {
        return almoxDestino;
    }

    public void setAlmoxDestino(String almoxDestino) {
        this.almoxDestino = almoxDestino;
    }
    
    
}
