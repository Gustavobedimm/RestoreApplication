package restoreapplication.Model;

public class Formulacao {
    private String empresa;
    private String idformulacao;
    private String formula;
    private String qtde;
    private String data;
    private String usuario;
    private String datafechamento;
    private String formulado;

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getIdformulacao() {
        return idformulacao;
    }

    public void setIdformulacao(String idformulacao) {
        this.idformulacao = idformulacao;
    }

    public String getFormula() {
        return formula;
    }

    public void setFormula(String formula) {
        this.formula = formula;
    }

    public String getQtde() {
        return qtde;
    }

    public void setQtde(String qtde) {
        this.qtde = qtde;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getDatafechamento() {
        return datafechamento;
    }

    public void setDatafechamento(String datafechamento) {
        this.datafechamento = datafechamento;
    }

    public String getFormulado() {
        return formulado;
    }

    public void setFormulado(String formulado) {
        this.formulado = formulado;
    }
    
}
