package restoreapplication.Model;
public class TransfEmpresaItem {
    private String idtransfempresaitens;
    private String idtransfempresa;
    private String produto;
    private String quantidade;

    public String getIdtransfempresaitens() {
        return idtransfempresaitens;
    }

    public void setIdtransfempresaitens(String idtransfempresaitens) {
        this.idtransfempresaitens = idtransfempresaitens;
    }

    public String getIdtransfempresa() {
        return idtransfempresa;
    }

    public void setIdtransfempresa(String idtransfempresa) {
        this.idtransfempresa = idtransfempresa;
    }

    public String getProduto() {
        return produto;
    }

    public void setProduto(String produto) {
        this.produto = produto;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }
    
}
